// Command kafkagen reads Kafka protocol message definitions from a directory
// of JSON specs and generates Go decoders into a target package directory.
//
// Each request/response/header/data spec produces one file (snake_case of the
// message name) with struct types and a Decode method. A dispatch.go file is
// also emitted with UnmarshalRequest and UnmarshalResponse top-level helpers.
//
//go:generate go run ./... -in ../../message -out ../../
package main

import (
	"bytes"
	"flag"
	"fmt"
	"go/format"
	"log"
	"os"
	"path/filepath"
	"sort"
	"strings"
)

func main() {
	var inDir, outDir string
	flag.StringVar(&inDir, "in", "message", "directory of Kafka JSON spec files")
	flag.StringVar(&outDir, "out", "kafkaproto", "directory to write generated Go files into")
	flag.Parse()

	if err := run(inDir, outDir); err != nil {
		log.Fatal(err)
	}
}

func run(inDir, outDir string) error {
	entries, err := os.ReadDir(inDir)
	if err != nil {
		return fmt.Errorf("read %s: %w", inDir, err)
	}

	// Clean previously generated files (anything tagged with our header).
	if err := cleanGenerated(outDir); err != nil {
		return err
	}

	var specs []*Spec
	for _, entry := range entries {
		if entry.IsDir() || !strings.HasSuffix(entry.Name(), ".json") {
			continue
		}
		path := filepath.Join(inDir, entry.Name())
		data, err := os.ReadFile(path)
		if err != nil {
			return fmt.Errorf("read %s: %w", path, err)
		}
		spec, err := ParseSpec(data)
		if err != nil {
			return fmt.Errorf("parse %s: %w", path, err)
		}
		if spec.Valid.None {
			// "validVersions": "none" — message is deprecated/unsupported.
			continue
		}
		specs = append(specs, spec)
	}

	sort.Slice(specs, func(i, j int) bool { return specs[i].Name < specs[j].Name })

	for _, spec := range specs {
		out, err := EmitMessageFile(spec)
		if err != nil {
			return fmt.Errorf("emit %s: %w", spec.Name, err)
		}
		path := filepath.Join(outDir, snakeCase(spec.Name)+".go")
		if err := os.WriteFile(path, out, 0o644); err != nil {
			return fmt.Errorf("write %s: %w", path, err)
		}
	}

	dispatch, err := EmitDispatch(specs)
	if err != nil {
		return fmt.Errorf("emit dispatch: %w", err)
	}
	path := filepath.Join(outDir, "dispatch.go")
	if err := os.WriteFile(path, dispatch, 0o644); err != nil {
		return fmt.Errorf("write %s: %w", path, err)
	}

	log.Printf("kafkagen: wrote %d message files + dispatch.go", len(specs))
	return nil
}

const generatedHeader = "// Code generated by cmd/kafkagen. DO NOT EDIT."

// cleanGenerated removes every .go file in outDir that begins with our generated
// header. Hand-written files (reader.go, gen.go, ...) are left alone.
func cleanGenerated(outDir string) error {
	entries, err := os.ReadDir(outDir)
	if err != nil {
		return fmt.Errorf("read %s: %w", outDir, err)
	}
	for _, entry := range entries {
		if entry.IsDir() || !strings.HasSuffix(entry.Name(), ".go") {
			continue
		}
		path := filepath.Join(outDir, entry.Name())
		data, err := os.ReadFile(path)
		if err != nil {
			return err
		}
		if bytes.HasPrefix(data, []byte(generatedHeader)) {
			if err := os.Remove(path); err != nil {
				return err
			}
		}
	}
	return nil
}

func snakeCase(s string) string {
	var b strings.Builder
	for i, r := range s {
		if i > 0 && r >= 'A' && r <= 'Z' {
			b.WriteByte('_')
		}
		if r >= 'A' && r <= 'Z' {
			b.WriteRune(r + ('a' - 'A'))
		} else {
			b.WriteRune(r)
		}
	}
	return b.String()
}

func gofmt(src []byte) ([]byte, error) {
	out, err := format.Source(src)
	if err != nil {
		// Return the unformatted source alongside the error so the user can see
		// which line tripped gofmt; helps debug generator bugs.
		return src, fmt.Errorf("gofmt: %w\n--- source ---\n%s", err, src)
	}
	return out, nil
}

// EmitDispatch generates the top-level UnmarshalRequest / UnmarshalResponse helpers.
func EmitDispatch(specs []*Spec) ([]byte, error) {
	e := &emitter{}
	e.line(generatedHeader)
	e.line("")
	e.line("package kafkaproto")
	e.line("")
	e.line("import \"fmt\"")
	e.line("")
	e.line("// Message is implemented by every generated request/response struct.")
	e.line("type Message interface {")
	e.line("\tDecode(r *Reader, version int16) error")
	e.line("\tEncode(w *Writer, version int16) error")
	e.line("}")
	e.line("")

	emitDispatchFn(e, "UnmarshalRequest", "request", specs)
	emitDispatchFn(e, "UnmarshalResponse", "response", specs)

	emitRequestHeaderCodec(e, specs)
	emitResponseHeaderCodec(e, specs)

	return gofmt(e.buf.Bytes())
}

// emitRequestHeaderCodec writes free-standing ReadRequestHeader and
// WriteRequestHeader helpers. For flexible (apiKey, apiVersion) combinations
// the read path consumes the trailing tagged-fields count and the write path
// emits an empty tagged-fields block. Flexibility is determined per apiKey
// from the request spec's flexibleVersions.
func emitRequestHeaderCodec(e *emitter, specs []*Spec) {
	e.line("// ReadRequestHeader decodes a Kafka request header. For flexible (apiKey,")
	e.line("// apiVersion) combinations it also consumes the trailing tagged-fields count,")
	e.line("// leaving r positioned at the start of the request body.")
	e.line("func ReadRequestHeader(r *Reader) (apiKey int16, apiVersion int16, corrID int32, clientID *string, err error) {")
	e.line("\tapiKey, err = r.ReadInt16()")
	e.line("\tif err != nil { return }")
	e.line("\tapiVersion, err = r.ReadInt16()")
	e.line("\tif err != nil { return }")
	e.line("\tcorrID, err = r.ReadInt32()")
	e.line("\tif err != nil { return }")
	e.line("\tclientID, err = r.ReadNullableString()")
	e.line("\tif err != nil { return }")
	e.line("\tif requestHeaderFlexible(apiKey, apiVersion) {")
	e.line("\t\tif _, err = r.ReadUvarint(); err != nil { return }")
	e.line("\t}")
	e.line("\treturn")
	e.line("}")
	e.line("")
	e.line("// WriteRequestHeader encodes a Kafka request header (apiKey, apiVersion,")
	e.line("// correlation id, client id). For flexible (apiKey, apiVersion) combinations")
	e.line("// it also writes an empty tagged-fields block.")
	e.line("func WriteRequestHeader(w *Writer, apiKey int16, apiVersion int16, corrID int32, clientID *string) {")
	e.line("\tw.WriteInt16(apiKey)")
	e.line("\tw.WriteInt16(apiVersion)")
	e.line("\tw.WriteInt32(corrID)")
	e.line("\tw.WriteNullableString(clientID)")
	e.line("\tif requestHeaderFlexible(apiKey, apiVersion) {")
	e.line("\t\tw.WriteUvarint(0)")
	e.line("\t}")
	e.line("}")
	e.line("")
	emitFlexibleSwitch(e, "requestHeaderFlexible", "request", specs)
}

// emitResponseHeaderCodec writes free-standing ReadResponseHeader and
// WriteResponseHeader helpers. For flexible (apiKey, apiVersion) combinations
// the read path consumes the trailing tagged-fields count and the write path
// emits an empty tagged-fields block. Flexibility is determined per apiKey
// from the response spec's flexibleVersions.
func emitResponseHeaderCodec(e *emitter, specs []*Spec) {
	e.line("// ReadResponseHeader decodes a Kafka response header. The wire form is just")
	e.line("// the correlation id (plus a tagged-fields count for flexible apiVersions),")
	e.line("// so the caller supplies a lookup that maps the just-read corrID back to the")
	e.line("// (apiKey, apiVersion) of the original request — typically by consulting an")
	e.line("// in-flight table — which is what we need to decide whether the header is")
	e.line("// flexible. A non-nil error from lookup is returned as-is.")
	e.line("func ReadResponseHeader(r *Reader, lookup func(corrID int32) (apiKey, apiVersion int16, err error)) (corrID int32, apiKey int16, apiVersion int16, err error) {")
	e.line("\tcorrID, err = r.ReadInt32()")
	e.line("\tif err != nil { return }")
	e.line("\tapiKey, apiVersion, err = lookup(corrID)")
	e.line("\tif err != nil { return }")
	e.line("\tif responseHeaderFlexible(apiKey, apiVersion) {")
	e.line("\t\tif _, err = r.ReadUvarint(); err != nil { return }")
	e.line("\t}")
	e.line("\treturn")
	e.line("}")
	e.line("")
	e.line("// WriteResponseHeader encodes a Kafka response header (correlation id, plus")
	e.line("// an empty tagged-fields block for flexible apiVersions). apiKey and")
	e.line("// apiVersion are only used to decide whether the header is flexible.")
	e.line("func WriteResponseHeader(w *Writer, apiKey int16, apiVersion int16, corrID int32) {")
	e.line("\tw.WriteInt32(corrID)")
	e.line("\tif responseHeaderFlexible(apiKey, apiVersion) {")
	e.line("\t\tw.WriteUvarint(0)")
	e.line("\t}")
	e.line("}")
	e.line("")
	emitFlexibleSwitch(e, "responseHeaderFlexible", "response", specs)
}

func emitFlexibleSwitch(e *emitter, fnName, kind string, specs []*Spec) {
	byKey := map[int]*Spec{}
	for _, s := range specs {
		if s.Type != kind || !s.HasAPIKey {
			continue
		}
		byKey[s.APIKey] = s
	}
	keys := make([]int, 0, len(byKey))
	for k := range byKey {
		keys = append(keys, k)
	}
	sort.Ints(keys)

	e.line("func %s(apiKey, apiVersion int16) bool {", fnName)
	e.line("\t_ = apiVersion")
	e.line("\tswitch apiKey {")
	for _, k := range keys {
		s := byKey[k]
		cond := s.Flexible.Condition("apiVersion")
		e.line("\tcase %d: // %s", k, s.Name)
		e.line("\t\treturn %s", cond)
	}
	e.line("\t}")
	e.line("\treturn false")
	e.line("}")
	e.line("")
}

func emitDispatchFn(e *emitter, fnName, kind string, specs []*Spec) {
	// Group by apiKey, picking the matching kind.
	byKey := map[int]*Spec{}
	for _, s := range specs {
		if s.Type != kind || !s.HasAPIKey {
			continue
		}
		byKey[s.APIKey] = s
	}
	keys := make([]int, 0, len(byKey))
	for k := range byKey {
		keys = append(keys, k)
	}
	sort.Ints(keys)

	e.line("// %s decodes a Kafka %s body from r (positioned after the request/response", fnName, kind)
	e.line("// header) into the appropriate generated struct, dispatched on apiKey.")
	e.line("func %s(r *Reader, apiKey, apiVersion int16) (Message, error) {", fnName)
	e.line("\tvar m Message")
	e.line("\tswitch apiKey {")
	for _, k := range keys {
		s := byKey[k]
		e.line("\tcase %d:", k)
		e.line("\t\tm = &%s{}", s.Name)
	}
	e.line("\tdefault:")
	e.line("\t\treturn nil, fmt.Errorf(\"kafkaproto: unsupported %s apiKey %%d\", apiKey)", kind)
	e.line("\t}")
	e.line("\tif err := m.Decode(r, apiVersion); err != nil {")
	e.line("\t\treturn nil, err")
	e.line("\t}")
	e.line("\treturn m, nil")
	e.line("}")
	e.line("")
}
