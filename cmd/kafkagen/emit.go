package main

import (
	"bytes"
	"fmt"
	"sort"
	"strings"
)

// goTypeName returns the Go-level name for a struct in this spec. The top-level
// message keeps its plain name; nested/common-struct types are namespaced with
// the message name as a prefix to avoid collisions across files (the spec
// re-uses names like "TopicData" in many places). If the inner name already
// starts with the outer name we don't double-prefix.
func goTypeName(specName, structName string) string {
	if structName == specName {
		return specName
	}
	if strings.HasPrefix(structName, specName) {
		return structName
	}
	return specName + structName
}

// goFieldType returns the Go type expression for a field.
func goFieldType(spec *Spec, f *Field) string {
	switch f.Kind {
	case KindBool:
		return "bool"
	case KindInt8:
		return "int8"
	case KindInt16:
		return "int16"
	case KindUint16:
		return "uint16"
	case KindInt32:
		return "int32"
	case KindUint32:
		return "uint32"
	case KindInt64:
		return "int64"
	case KindFloat64:
		return "float64"
	case KindUUID:
		return "[16]byte"
	case KindString:
		// Use *string when the field is ever nullable, to preserve null vs "".
		if !f.Nullable.None {
			return "*string"
		}
		return "string"
	case KindBytes, KindRecords:
		return "[]byte"
	case KindArray:
		return "[]" + goFieldType(spec, f.Elem)
	case KindStruct:
		return goTypeName(spec.Name, f.StructName)
	}
	return "any"
}

// emitter buffers code as we go, then run gofmt at the end.
type emitter struct {
	buf bytes.Buffer
}

func (e *emitter) line(format string, args ...any) {
	fmt.Fprintf(&e.buf, format, args...)
	e.buf.WriteByte('\n')
}

// EmitMessageFile generates the Go source for one message spec.
func EmitMessageFile(spec *Spec) ([]byte, error) {
	e := &emitter{}
	e.line("// Code generated by cmd/kafkagen. DO NOT EDIT.")
	e.line("")
	e.line("package kafkaproto")
	e.line("")
	e.line("import (")
	e.line("\t\"fmt\"")
	e.line(")")
	e.line("")

	// Emit struct types in stable order: top-level first, then others sorted by name.
	emitStructDecl(e, spec, spec.Name)
	others := make([]string, 0, len(spec.Structs))
	for name := range spec.Structs {
		if name == spec.Name {
			continue
		}
		others = append(others, name)
	}
	sort.Strings(others)
	for _, name := range others {
		emitStructDecl(e, spec, name)
	}

	// Top-level Decode/Encode methods own the byte slice and wrap errors.
	emitTopLevelDecode(e, spec)
	emitTopLevelEncode(e, spec)

	// Per-struct decode/encode helpers (used for nested + top-level alike).
	emitStructDecodeFn(e, spec, spec.Name, spec.Fields)
	for _, name := range others {
		s := spec.Structs[name]
		emitStructDecodeFn(e, spec, name, s.Fields)
	}
	emitStructEncodeFn(e, spec, spec.Name, spec.Fields)
	for _, name := range others {
		s := spec.Structs[name]
		emitStructEncodeFn(e, spec, name, s.Fields)
	}

	return gofmt(e.buf.Bytes())
}

func emitStructDecl(e *emitter, spec *Spec, structName string) {
	var fields []*Field
	if structName == spec.Name {
		fields = spec.Fields
	} else if s, ok := spec.Structs[structName]; ok {
		fields = s.Fields
	}
	e.line("type %s struct {", goTypeName(spec.Name, structName))
	for _, f := range fields {
		e.line("\t%s %s", f.Name, goFieldType(spec, f))
	}
	e.line("}")
	e.line("")
}

func emitTopLevelDecode(e *emitter, spec *Spec) {
	e.line("// Decode reads m from r at the given protocol version. r must be positioned")
	e.line("// at the start of the message body (after any request/response header) and")
	e.line("// must contain exactly one message — trailing bytes return ErrTrailingBytes.")
	e.line("func (m *%s) Decode(r *Reader, version int16) error {", spec.Name)
	e.line("\tif err := m.decode(r, version); err != nil {")
	e.line("\t\treturn fmt.Errorf(\"%s v%%d: %%w\", version, err)", spec.Name)
	e.line("\t}")
	e.line("\tif n := r.Remaining(); n > 0 {")
	e.line("\t\treturn fmt.Errorf(\"%s v%%d: %%w (%%d byte(s) left)\", version, ErrTrailingBytes, n)", spec.Name)
	e.line("\t}")
	e.line("\treturn nil")
	e.line("}")
	e.line("")
}

func emitStructDecodeFn(e *emitter, spec *Spec, structName string, fields []*Field) {
	goName := goTypeName(spec.Name, structName)
	e.line("func (m *%s) decode(r *Reader, version int16) error {", goName)
	e.line("\t_ = version")

	flexCond := spec.Flexible.Condition("version")
	switch flexCond {
	case "true":
		e.line("\tflexible := true")
	case "false":
		e.line("\tflexible := false")
	default:
		e.line("\tflexible := %s", flexCond)
	}
	e.line("\t_ = flexible")

	// First pass: regular (non-tagged) fields, in declaration order.
	for _, f := range fields {
		if isTagged(f) {
			continue
		}
		emitFieldDecode(e, spec, "m."+f.Name, f, "version", "flexible", "r", false)
	}

	// Second pass: tagged fields, only present in flexible mode.
	tagged := collectTagged(fields)
	if len(tagged) == 0 {
		e.line("\tif flexible {")
		e.line("\t\tif err := r.ReadTaggedFields(nil); err != nil { return err }")
		e.line("\t}")
	} else {
		e.line("\tif flexible {")
		e.line("\t\tif err := r.ReadTaggedFields(func(tag uint64, sub *Reader) error {")
		e.line("\t\t\tswitch tag {")
		for _, f := range tagged {
			e.line("\t\t\tcase %d:", f.Tag)
			e.line("\t\t\t\tif !(%s) { return nil }", versionGuard(f, "version"))
			emitFieldDecode(e, spec, "m."+f.Name, f, "version", "true", "sub", true)
		}
		e.line("\t\t\t}")
		e.line("\t\t\treturn nil")
		e.line("\t\t}); err != nil { return err }")
		e.line("\t}")
	}

	e.line("\treturn nil")
	e.line("}")
	e.line("")
}

func isTagged(f *Field) bool { return f.HasTag && !f.Tagged.None }

func collectTagged(fields []*Field) []*Field {
	var out []*Field
	for _, f := range fields {
		if isTagged(f) {
			out = append(out, f)
		}
	}
	sort.Slice(out, func(i, j int) bool { return out[i].Tag < out[j].Tag })
	return out
}

// versionGuard returns the boolean expression that gates a field on the active
// protocol version (intersection of versions and, for tagged fields, taggedVersions).
func versionGuard(f *Field, varName string) string {
	rg := f.Versions
	if isTagged(f) {
		rg = rg.Intersect(f.Tagged)
	}
	return rg.Condition(varName)
}

// emitFieldDecode writes the code that decodes one field (or one element when
// inTagged is true and we're inside a tagged-field handler — same logic). The
// flexFlag arg is the Go-level expression that evaluates to whether flexible
// encoding applies (typically "flexible", or "true" inside tagged handlers).
func emitFieldDecode(e *emitter, spec *Spec, target string, f *Field, versionVar, flexFlag, readerVar string, insideTagged bool) {
	cond := f.Versions.Condition(versionVar)

	// Tagged fields are gated on taggedVersions inside the handler — done by
	// the caller. Skip the version gate emission in that case.
	if !insideTagged && cond != "true" {
		e.line("\tif %s {", cond)
		emitFieldBody(e, spec, target, f, versionVar, flexFlag, readerVar)
		e.line("\t}")
		return
	}
	if cond == "false" {
		return
	}
	emitFieldBody(e, spec, target, f, versionVar, flexFlag, readerVar)
}

func emitFieldBody(e *emitter, spec *Spec, target string, f *Field, versionVar, flexFlag, readerVar string) {
	nullableCond := f.Nullable.Condition(versionVar)
	switch f.Kind {
	case KindBool:
		e.line("\t{ v, err := %s.ReadBool(); if err != nil { return err }; %s = v }", readerVar, target)
	case KindInt8:
		e.line("\t{ v, err := %s.ReadInt8(); if err != nil { return err }; %s = v }", readerVar, target)
	case KindInt16:
		e.line("\t{ v, err := %s.ReadInt16(); if err != nil { return err }; %s = v }", readerVar, target)
	case KindUint16:
		e.line("\t{ v, err := %s.ReadUint16(); if err != nil { return err }; %s = v }", readerVar, target)
	case KindInt32:
		e.line("\t{ v, err := %s.ReadInt32(); if err != nil { return err }; %s = v }", readerVar, target)
	case KindUint32:
		e.line("\t{ v, err := %s.ReadUint32(); if err != nil { return err }; %s = v }", readerVar, target)
	case KindInt64:
		e.line("\t{ v, err := %s.ReadInt64(); if err != nil { return err }; %s = v }", readerVar, target)
	case KindFloat64:
		e.line("\t{ v, err := %s.ReadFloat64(); if err != nil { return err }; %s = v }", readerVar, target)
	case KindUUID:
		e.line("\t{ v, err := %s.ReadUUID(); if err != nil { return err }; %s = v }", readerVar, target)

	case KindString:
		// Field type is `string` if never nullable, else `*string`.
		isPtr := !f.Nullable.None
		if !isPtr {
			// Always non-nullable.
			e.line("\tif %s {", flexFlag)
			e.line("\t\tv, err := %s.ReadCompactString(); if err != nil { return err }; %s = v", readerVar, target)
			e.line("\t} else {")
			e.line("\t\tv, err := %s.ReadString(); if err != nil { return err }; %s = v", readerVar, target)
			e.line("\t}")
			return
		}
		// Field can be nullable in some versions. We need to choose between
		// nullable-vs-not at runtime if nullableVersions is a subset of versions.
		switch nullableCond {
		case "true":
			e.line("\tif %s {", flexFlag)
			e.line("\t\tv, err := %s.ReadCompactNullableString(); if err != nil { return err }; %s = v", readerVar, target)
			e.line("\t} else {")
			e.line("\t\tv, err := %s.ReadNullableString(); if err != nil { return err }; %s = v", readerVar, target)
			e.line("\t}")
		case "false":
			e.line("\tif %s {", flexFlag)
			e.line("\t\tv, err := %s.ReadCompactString(); if err != nil { return err }; s := v; %s = &s", readerVar, target)
			e.line("\t} else {")
			e.line("\t\tv, err := %s.ReadString(); if err != nil { return err }; s := v; %s = &s", readerVar, target)
			e.line("\t}")
		default:
			e.line("\tif %s {", nullableCond)
			e.line("\t\tif %s {", flexFlag)
			e.line("\t\t\tv, err := %s.ReadCompactNullableString(); if err != nil { return err }; %s = v", readerVar, target)
			e.line("\t\t} else {")
			e.line("\t\t\tv, err := %s.ReadNullableString(); if err != nil { return err }; %s = v", readerVar, target)
			e.line("\t\t}")
			e.line("\t} else {")
			e.line("\t\tif %s {", flexFlag)
			e.line("\t\t\tv, err := %s.ReadCompactString(); if err != nil { return err }; s := v; %s = &s", readerVar, target)
			e.line("\t\t} else {")
			e.line("\t\t\tv, err := %s.ReadString(); if err != nil { return err }; s := v; %s = &s", readerVar, target)
			e.line("\t\t}")
			e.line("\t}")
		}

	case KindBytes, KindRecords:
		// Stored as []byte; nil = null. Reader returns nil for null length, slice otherwise.
		// (For non-nullable bytes the wire is the same except length cannot be -1; we trust the spec.)
		e.line("\tif %s {", flexFlag)
		e.line("\t\tv, err := %s.ReadCompactBytes(); if err != nil { return err }; %s = v", readerVar, target)
		e.line("\t} else {")
		e.line("\t\tv, err := %s.ReadBytes(); if err != nil { return err }; %s = v", readerVar, target)
		e.line("\t}")

	case KindArray:
		emitArrayDecode(e, spec, target, f, versionVar, flexFlag, readerVar)

	case KindStruct:
		// A bare struct field (rare — most struct fields appear as []T arrays) is
		// decoded by calling its decode method on the existing reader.
		e.line("\tif err := %s.decode(%s, %s); err != nil { return err }", target, readerVar, versionVar)
	}
}

func emitArrayDecode(e *emitter, spec *Spec, target string, f *Field, versionVar, flexFlag, readerVar string) {
	elem := f.Elem
	elemType := goFieldType(spec, elem)

	e.line("\t{")
	e.line("\t\tvar n int; var err error")
	e.line("\t\tif %s {", flexFlag)
	e.line("\t\t\tn, err = %s.ReadCompactArrayLen()", readerVar)
	e.line("\t\t} else {")
	e.line("\t\t\tn, err = %s.ReadArrayLen()", readerVar)
	e.line("\t\t}")
	e.line("\t\tif err != nil { return err }")
	e.line("\t\tif n >= 0 {")
	e.line("\t\t\t%s = make([]%s, n)", target, elemType)
	e.line("\t\t\tfor i := 0; i < n; i++ {")
	emitElemDecode(e, spec, fmt.Sprintf("%s[i]", target), elem, versionVar, flexFlag, readerVar)
	e.line("\t\t\t}")
	e.line("\t\t}")
	e.line("\t}")
}

// emitElemDecode is like emitFieldBody but for an array element (no version gate).
func emitElemDecode(e *emitter, spec *Spec, target string, f *Field, versionVar, flexFlag, readerVar string) {
	switch f.Kind {
	case KindStruct:
		e.line("\t\t\t\tif err := %s.decode(%s, %s); err != nil { return err }", target, readerVar, versionVar)
	case KindString:
		// Arrays of nullable strings would be unusual; spec uses []string only as non-nullable.
		e.line("\t\t\t\tif %s {", flexFlag)
		e.line("\t\t\t\t\tv, err := %s.ReadCompactString(); if err != nil { return err }; %s = v", readerVar, target)
		e.line("\t\t\t\t} else {")
		e.line("\t\t\t\t\tv, err := %s.ReadString(); if err != nil { return err }; %s = v", readerVar, target)
		e.line("\t\t\t\t}")
	case KindBool:
		e.line("\t\t\t\t{ v, err := %s.ReadBool(); if err != nil { return err }; %s = v }", readerVar, target)
	case KindInt8:
		e.line("\t\t\t\t{ v, err := %s.ReadInt8(); if err != nil { return err }; %s = v }", readerVar, target)
	case KindInt16:
		e.line("\t\t\t\t{ v, err := %s.ReadInt16(); if err != nil { return err }; %s = v }", readerVar, target)
	case KindUint16:
		e.line("\t\t\t\t{ v, err := %s.ReadUint16(); if err != nil { return err }; %s = v }", readerVar, target)
	case KindInt32:
		e.line("\t\t\t\t{ v, err := %s.ReadInt32(); if err != nil { return err }; %s = v }", readerVar, target)
	case KindUint32:
		e.line("\t\t\t\t{ v, err := %s.ReadUint32(); if err != nil { return err }; %s = v }", readerVar, target)
	case KindInt64:
		e.line("\t\t\t\t{ v, err := %s.ReadInt64(); if err != nil { return err }; %s = v }", readerVar, target)
	case KindFloat64:
		e.line("\t\t\t\t{ v, err := %s.ReadFloat64(); if err != nil { return err }; %s = v }", readerVar, target)
	case KindUUID:
		e.line("\t\t\t\t{ v, err := %s.ReadUUID(); if err != nil { return err }; %s = v }", readerVar, target)
	default:
		e.line("\t\t\t\treturn fmt.Errorf(\"unsupported array element kind\")")
	}
}

// ---------- encode side ----------

func emitTopLevelEncode(e *emitter, spec *Spec) {
	e.line("// Encode serializes m into w at the given protocol version (the bytes")
	e.line("// appended are the message body only, no request/response header).")
	e.line("func (m *%s) Encode(w *Writer, version int16) error {", spec.Name)
	e.line("\tif err := m.encode(w, version); err != nil {")
	e.line("\t\treturn fmt.Errorf(\"%s v%%d: %%w\", version, err)", spec.Name)
	e.line("\t}")
	e.line("\treturn nil")
	e.line("}")
	e.line("")
}

func emitStructEncodeFn(e *emitter, spec *Spec, structName string, fields []*Field) {
	goName := goTypeName(spec.Name, structName)
	e.line("func (m *%s) encode(w *Writer, version int16) error {", goName)
	e.line("\t_ = version")

	flexCond := spec.Flexible.Condition("version")
	switch flexCond {
	case "true":
		e.line("\tflexible := true")
	case "false":
		e.line("\tflexible := false")
	default:
		e.line("\tflexible := %s", flexCond)
	}
	e.line("\t_ = flexible")

	for _, f := range fields {
		if isTagged(f) {
			continue
		}
		emitFieldEncode(e, spec, "m."+f.Name, f, "version", "flexible", "w", false)
	}

	tagged := collectTagged(fields)
	if len(tagged) == 0 {
		e.line("\tif flexible {")
		e.line("\t\tw.WriteTaggedFields(nil)")
		e.line("\t}")
	} else {
		e.line("\tif flexible {")
		e.line("\t\tvar tfs []TaggedField")
		for _, f := range tagged {
			e.line("\t\tif %s {", versionGuard(f, "version"))
			e.line("\t\t\tsub := NewWriter()")
			emitFieldEncode(e, spec, "m."+f.Name, f, "version", "true", "sub", true)
			e.line("\t\t\ttfs = append(tfs, TaggedField{Tag: %d, Body: sub.Bytes()})", f.Tag)
			e.line("\t\t}")
		}
		e.line("\t\tw.WriteTaggedFields(tfs)")
		e.line("\t}")
	}

	e.line("\treturn nil")
	e.line("}")
	e.line("")
}

func emitFieldEncode(e *emitter, spec *Spec, target string, f *Field, versionVar, flexFlag, writerVar string, insideTagged bool) {
	cond := f.Versions.Condition(versionVar)
	if !insideTagged && cond != "true" {
		e.line("\tif %s {", cond)
		emitFieldEncodeBody(e, spec, target, f, versionVar, flexFlag, writerVar)
		e.line("\t}")
		return
	}
	if cond == "false" {
		return
	}
	emitFieldEncodeBody(e, spec, target, f, versionVar, flexFlag, writerVar)
}

func emitFieldEncodeBody(e *emitter, spec *Spec, target string, f *Field, versionVar, flexFlag, writerVar string) {
	switch f.Kind {
	case KindBool:
		e.line("\t%s.WriteBool(%s)", writerVar, target)
	case KindInt8:
		e.line("\t%s.WriteInt8(%s)", writerVar, target)
	case KindInt16:
		e.line("\t%s.WriteInt16(%s)", writerVar, target)
	case KindUint16:
		e.line("\t%s.WriteUint16(%s)", writerVar, target)
	case KindInt32:
		e.line("\t%s.WriteInt32(%s)", writerVar, target)
	case KindUint32:
		e.line("\t%s.WriteUint32(%s)", writerVar, target)
	case KindInt64:
		e.line("\t%s.WriteInt64(%s)", writerVar, target)
	case KindFloat64:
		e.line("\t%s.WriteFloat64(%s)", writerVar, target)
	case KindUUID:
		e.line("\t%s.WriteUUID(%s)", writerVar, target)

	case KindString:
		isPtr := !f.Nullable.None
		nullableCond := f.Nullable.Condition(versionVar)
		if !isPtr {
			e.line("\tif %s { %s.WriteCompactString(%s) } else { %s.WriteString(%s) }",
				flexFlag, writerVar, target, writerVar, target)
			return
		}
		switch nullableCond {
		case "true":
			e.line("\tif %s { %s.WriteCompactNullableString(%s) } else { %s.WriteNullableString(%s) }",
				flexFlag, writerVar, target, writerVar, target)
		case "false":
			// In a version where this string is *string typed but not nullable, we
			// dereference (callers must set it). We still respect flexFlag.
			e.line("\tif %s == nil { return fmt.Errorf(\"%s: nil at non-nullable version\") }", target, f.Name)
			e.line("\tif %s { %s.WriteCompactString(*%s) } else { %s.WriteString(*%s) }",
				flexFlag, writerVar, target, writerVar, target)
		default:
			e.line("\tif %s {", nullableCond)
			e.line("\t\tif %s { %s.WriteCompactNullableString(%s) } else { %s.WriteNullableString(%s) }",
				flexFlag, writerVar, target, writerVar, target)
			e.line("\t} else {")
			e.line("\t\tif %s == nil { return fmt.Errorf(\"%s: nil at non-nullable version\") }", target, f.Name)
			e.line("\t\tif %s { %s.WriteCompactString(*%s) } else { %s.WriteString(*%s) }",
				flexFlag, writerVar, target, writerVar, target)
			e.line("\t}")
		}

	case KindBytes, KindRecords:
		// Reader returns nil for null length; writer mirrors that — nil → null.
		e.line("\tif %s { %s.WriteCompactBytes(%s) } else { %s.WriteBytes(%s) }",
			flexFlag, writerVar, target, writerVar, target)

	case KindArray:
		emitArrayEncode(e, spec, target, f, versionVar, flexFlag, writerVar)

	case KindStruct:
		e.line("\tif err := %s.encode(%s, %s); err != nil { return err }", target, writerVar, versionVar)
	}
}

func emitArrayEncode(e *emitter, spec *Spec, target string, f *Field, versionVar, flexFlag, writerVar string) {
	nullableCond := f.Nullable.Condition(versionVar)

	e.line("\t{")
	switch nullableCond {
	case "true":
		e.line("\t\tif %s == nil {", target)
		e.line("\t\t\tif %s { %s.WriteCompactArrayLen(-1) } else { %s.WriteArrayLen(-1) }", flexFlag, writerVar, writerVar)
		e.line("\t\t} else {")
		emitArrayBody(e, spec, target, f, versionVar, flexFlag, writerVar)
		e.line("\t\t}")
	case "false":
		emitArrayBody(e, spec, target, f, versionVar, flexFlag, writerVar)
	default:
		e.line("\t\tif (%s) && %s == nil {", nullableCond, target)
		e.line("\t\t\tif %s { %s.WriteCompactArrayLen(-1) } else { %s.WriteArrayLen(-1) }", flexFlag, writerVar, writerVar)
		e.line("\t\t} else {")
		emitArrayBody(e, spec, target, f, versionVar, flexFlag, writerVar)
		e.line("\t\t}")
	}
	e.line("\t}")
}

func emitArrayBody(e *emitter, spec *Spec, target string, f *Field, versionVar, flexFlag, writerVar string) {
	e.line("\t\t\tn := len(%s)", target)
	e.line("\t\t\tif %s { %s.WriteCompactArrayLen(n) } else { %s.WriteArrayLen(n) }",
		flexFlag, writerVar, writerVar)
	e.line("\t\t\tfor i := 0; i < n; i++ {")
	emitElemEncode(e, spec, fmt.Sprintf("%s[i]", target), f.Elem, versionVar, flexFlag, writerVar)
	e.line("\t\t\t}")
}

func emitElemEncode(e *emitter, spec *Spec, target string, f *Field, versionVar, flexFlag, writerVar string) {
	switch f.Kind {
	case KindStruct:
		e.line("\t\t\t\tif err := %s.encode(%s, %s); err != nil { return err }", target, writerVar, versionVar)
	case KindString:
		e.line("\t\t\t\tif %s { %s.WriteCompactString(%s) } else { %s.WriteString(%s) }",
			flexFlag, writerVar, target, writerVar, target)
	case KindBool:
		e.line("\t\t\t\t%s.WriteBool(%s)", writerVar, target)
	case KindInt8:
		e.line("\t\t\t\t%s.WriteInt8(%s)", writerVar, target)
	case KindInt16:
		e.line("\t\t\t\t%s.WriteInt16(%s)", writerVar, target)
	case KindUint16:
		e.line("\t\t\t\t%s.WriteUint16(%s)", writerVar, target)
	case KindInt32:
		e.line("\t\t\t\t%s.WriteInt32(%s)", writerVar, target)
	case KindUint32:
		e.line("\t\t\t\t%s.WriteUint32(%s)", writerVar, target)
	case KindInt64:
		e.line("\t\t\t\t%s.WriteInt64(%s)", writerVar, target)
	case KindFloat64:
		e.line("\t\t\t\t%s.WriteFloat64(%s)", writerVar, target)
	case KindUUID:
		e.line("\t\t\t\t%s.WriteUUID(%s)", writerVar, target)
	default:
		e.line("\t\t\t\treturn fmt.Errorf(\"unsupported array element kind\")")
	}
}
